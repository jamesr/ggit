import "testing"

func TestParsePack(t *testing.T) {
	p, err := parsePackFile(testPack)
	if err != nil {
		t.Error("error parsing pack: ", err)
	}
	if p.numObjects != 7 {
		t.Errorf("expected 7 objects in pack file, got %d", p.numObjects)
	}
}

func TestParsePackIndex(t *testing.T) {
	idx, err := parsePackIndexFile(testIndex)
	if err != nil {
		t.Error("error parsing index: ", err)
	}
	if idx.numEntries != 7 {
		t.Errorf("expected 7 entries in index, got %d", idx.numEntries)
	}
}

